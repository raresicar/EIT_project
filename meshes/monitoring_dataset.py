class EITMonitoringDataset(Dataset):
    """
    Dataset for EIT stroke monitoring.

    Args:
        data_dir:     Root of monitoring_data/ (contains 6layer/, 3layer/)
        layer_type:   '6layer' or '3layer'
        subjects:     List of subject names, or None for all
        split:        'train', 'val', or 'test'
        train_frac:   Fraction of samples for training (default 0.8)
        val_frac:     Fraction for validation (default 0.1)
        seed:         Seed for train/val/test split
        flatten_U:    If True, flatten (n_patterns, n_electrodes) → (n_measurements,)
    """

    def __init__(
        self,
        data_dir: str = None,
        layer_type: str = "6layer",
        subjects: Optional[List[str]] = None,
        split: str = "train",
        train_frac: float = 0.80,
        val_frac: float = 0.10,
        seed: int = 42,
        flatten_U: bool = True,
    ):
        if data_dir is None:
            data_dir = str(MONITORING_DATA_DIR)

        self.data_dir = Path(data_dir) / layer_type
        self.layer_type = layer_type
        self.flatten_U = flatten_U

        # Discover subjects
        if subjects is None:
            subjects = sorted(
                d.name for d in self.data_dir.iterdir()
                if d.is_dir() and d.name.startswith("subject_")
            )

        # Collect all sample paths
        all_paths = []
        for subj in subjects:
            subj_dir = self.data_dir / subj
            sample_files = sorted(subj_dir.glob("sample_*.npz"))
            for f in sample_files:
                all_paths.append((subj, f))

        if len(all_paths) == 0:
            raise FileNotFoundError(
                f"No samples found in {self.data_dir} for subjects {subjects}"
            )

        # Deterministic split
        rng = np.random.RandomState(seed)
        indices = rng.permutation(len(all_paths))

        n_train = int(len(all_paths) * train_frac)
        n_val = int(len(all_paths) * val_frac)

        if split == "train":
            sel = indices[:n_train]
        elif split == "val":
            sel = indices[n_train : n_train + n_val]
        else:
            sel = indices[n_train + n_val :]

        self.samples = [all_paths[i] for i in sel]
        print(
            f"EITMonitoringDataset [{split}]: "
            f"{len(self.samples)} samples from {len(subjects)} subjects"
        )

        # Load Jacobians (one per subject — cached)
        self._jacobians = {}
        self._sigma_refs = {}
        for subj in subjects:
            jac_path = self.data_dir / subj / "jacobian.npz"
            if jac_path.exists():
                jac_data = np.load(jac_path)
                self._jacobians[subj] = jac_data["J"]
                self._sigma_refs[subj] = jac_data["sigma_ref"]

    def __len__(self):
        return len(self.samples)

    def __getitem__(self, idx):
        subj, path = self.samples[idx]
        data = np.load(path, allow_pickle=True)

        delta_U = data["delta_U"].astype(np.float32)
        if self.flatten_U:
            delta_U = delta_U.ravel()

        delta_sigma = data["delta_sigma_inv"].astype(np.float32)
        sigma_ref = data["sigma1_inv"].astype(np.float32)

        return {
            "delta_U": torch.from_numpy(delta_U),
            "delta_sigma": torch.from_numpy(delta_sigma),
            "sigma_ref": torch.from_numpy(sigma_ref),
            "subject": subj,
        }

    def get_jacobian(self, subject: str) -> np.ndarray:
        """Get the Jacobian for a given subject (numpy array)."""
        return self._jacobians[subject]

    def get_all_jacobians(self) -> dict:
        """Return dict subject → J (numpy)."""
        return dict(self._jacobians)

    @property
    def n_measurements(self) -> int:
        """Number of measurements (flattened ΔU length)."""
        sample = np.load(self.samples[0][1])
        return sample["delta_U"].size

    @property
    def n_elements_inv(self) -> int:
        """Number of elements on the inverse mesh."""
        sample = np.load(self.samples[0][1])
        return sample["delta_sigma_inv"].shape[0]


def create_dataloaders(
    data_dir: str = None,
    layer_type: str = "6layer",
    batch_size: int = 32,
    num_workers: int = 4,
    **dataset_kwargs,
) -> Tuple[DataLoader, DataLoader, DataLoader]:
    """Convenience function to create train/val/test loaders."""
    if data_dir is None:
        data_dir = str(MONITORING_DATA_DIR)

    train_ds = EITMonitoringDataset(
        data_dir, layer_type, split="train", **dataset_kwargs
    )
    val_ds = EITMonitoringDataset(
        data_dir, layer_type, split="val", **dataset_kwargs
    )
    test_ds = EITMonitoringDataset(
        data_dir, layer_type, split="test", **dataset_kwargs
    )

    def collate_fn(batch):
        return {
            "delta_U": torch.stack([b["delta_U"] for b in batch]),
            "delta_sigma": torch.stack([b["delta_sigma"] for b in batch]),
            "sigma_ref": torch.stack([b["sigma_ref"] for b in batch]),
            "subject": [b["subject"] for b in batch],
        }

    train_loader = DataLoader(
        train_ds, batch_size=batch_size, shuffle=True,
        num_workers=num_workers, collate_fn=collate_fn,
    )
    val_loader = DataLoader(
        val_ds, batch_size=batch_size, shuffle=False,
        num_workers=num_workers, collate_fn=collate_fn,
    )
    test_loader = DataLoader(
        test_ds, batch_size=batch_size, shuffle=False,
        num_workers=num_workers, collate_fn=collate_fn,
    )

    return train_loader, val_loader, test_loader